import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { ensureProjectFiles, saveConfig } from './project.js';
import { updateHandoffFromTranscript } from './handoff.js';
import { estimateTokens, hashText } from './token-utils.js';

const DEFAULT_TRANSCRIPT_TOKEN_THRESHOLD = 100000;
const DEFAULT_NOTICE_TTL_MS = 30 * 60 * 1000;
const DEFAULT_MAX_HANDOFF_TOKENS = 3500;

export function maybePrepareResetNotice(projectRoot = process.cwd(), input = {}, config = {}, options = {}) {
  const resetConfig = {
    enabled: true,
    transcriptTokenThreshold: DEFAULT_TRANSCRIPT_TOKEN_THRESHOLD,
    noticeTtlMs: DEFAULT_NOTICE_TTL_MS,
    maxHandoffTokens: DEFAULT_MAX_HANDOFF_TOKENS,
    ...(config.resetAssistant || {})
  };

  if (resetConfig.enabled === false) {
    return { ready: false, notice: '', reason: 'disabled' };
  }

  const transcriptPath = expandHome(input.transcript_path || input.transcriptPath || '');
  const prompt = String(input.prompt || input.user_prompt || input.message || '');
  const userAskedAboutReset = looksLikeResetIntent(prompt);
  const transcriptTokens = estimateTranscriptTokens(transcriptPath);

  if (!userAskedAboutReset && transcriptTokens < Number(resetConfig.transcriptTokenThreshold || DEFAULT_TRANSCRIPT_TOKEN_THRESHOLD)) {
    return {
      ready: false,
      notice: '',
      reason: 'below_threshold',
      transcriptTokens
    };
  }

  const paths = ensureProjectFiles(projectRoot);

  let handoffUpdate = null;

  if (transcriptPath && fs.existsSync(transcriptPath)) {
    handoffUpdate = updateHandoffFromTranscript(projectRoot, input, {
      source: options.source || 'ResetAssistant'
    });
  }

  const handoffText = readFile(paths.handoff);
  const handoffTokens = estimateTokens(handoffText);
  const useful = isUsefulHandoff(handoffText);

  if (!useful) {
    writeResetReadyFile(paths, {
      ready: false,
      transcriptTokens,
      handoffTokens,
      reason: 'handoff_not_useful',
      source: options.source || 'unknown'
    });

    return {
      ready: false,
      notice: '',
      reason: 'handoff_not_useful',
      transcriptTokens,
      handoffTokens,
      handoffUpdate
    };
  }

  if (handoffTokens > Number(resetConfig.maxHandoffTokens || DEFAULT_MAX_HANDOFF_TOKENS)) {
    writeResetReadyFile(paths, {
      ready: false,
      transcriptTokens,
      handoffTokens,
      reason: 'handoff_too_large',
      source: options.source || 'unknown'
    });

    return {
      ready: false,
      notice: '',
      reason: 'handoff_too_large',
      transcriptTokens,
      handoffTokens,
      handoffUpdate
    };
  }

  const notice = buildResetNotice(transcriptTokens);
  const noticeHash = hashText(`${transcriptPath}:${Math.floor(transcriptTokens / 10000)}:${hashText(handoffText).slice(0, 12)}`);
  const now = Date.now();
  const lastAt = config.state?.lastResetNoticeAt ? Date.parse(config.state.lastResetNoticeAt) : 0;
  const sameNotice = config.state?.lastResetNoticeHash === noticeHash;
  const withinTtl = lastAt && now - lastAt < Number(resetConfig.noticeTtlMs || DEFAULT_NOTICE_TTL_MS);

  writeResetReadyFile(paths, {
    ready: true,
    transcriptTokens,
    handoffTokens,
    reason: 'ready',
    source: options.source || 'unknown'
  });

  if (sameNotice && withinTtl) {
    return {
      ready: true,
      notice: '',
      reason: 'notice_deduped',
      transcriptTokens,
      handoffTokens,
      handoffUpdate
    };
  }

  saveConfig(projectRoot, {
    ...config,
    state: {
      ...(config.state || {}),
      lastResetNoticeHash: noticeHash,
      lastResetNoticeAt: new Date(now).toISOString()
    }
  });

  return {
    ready: true,
    notice,
    reason: 'notice_ready',
    transcriptTokens,
    handoffTokens,
    handoffUpdate
  };
}

function buildResetNotice(transcriptTokens) {
  const sessionPart = transcriptTokens > 0
    ? ` Estimated session context: ~${formatCompact(transcriptTokens)} tokens.`
    : '';

  return `Token Guard handoff is ready at TokenGuard/sessions/handoff.md.${sessionPart} If this is a new task, /clear can reduce context without starting from zero.`;
}

function isUsefulHandoff(text = '') {
  const raw = String(text || '').trim();

  if (!raw) return false;
  if (/No handoff has been generated yet/i.test(raw)) return false;

  const contentLines = raw
    .split(/\r?\n/)
    .map(line => line.trim())
    .filter(Boolean)
    .filter(line => !/^#/.test(line))
    .filter(line => !/^Generated:/.test(line))
    .filter(line => !/^Generated by:/.test(line));

  if (contentLines.length < 4) return false;

  return /Current Goal|Files Touched|Commands Run|Next Smallest Task/i.test(raw);
}

function writeResetReadyFile(paths, info) {
  const file = path.join(paths.sessions, 'reset-ready.md');

  const content = `# Session Reset Assistant

Generated: ${new Date().toISOString()}
Ready: ${info.ready ? 'yes' : 'no'}
Reason: ${info.reason}
Source: ${info.source}
Estimated transcript tokens: ${format(info.transcriptTokens || 0)}
Handoff tokens: ${format(info.handoffTokens || 0)}

${info.ready
    ? 'Recommendation: If the next request is a new task, /clear can reduce context while TokenGuard/sessions/handoff.md preserves continuity.'
    : 'Recommendation: do not suggest /clear based on Token Guard handoff readiness yet.'}
`;

  fs.writeFileSync(file, content);
}

function looksLikeResetIntent(prompt = '') {
  const raw = String(prompt || '');
  const s = raw.toLowerCase();

  return (
    s.includes('/clear') ||
    s.includes('new task') ||
    s.includes('fresh session') ||
    s.includes('clear context') ||
    s.includes('start a new session') ||
    /清空上下文|新任务|开新会话|新的会话|重新开一个会话|上下文太长/.test(raw)
  );
}

function estimateTranscriptTokens(transcriptPath) {
  if (!transcriptPath || !fs.existsSync(transcriptPath)) return 0;

  try {
    const stat = fs.statSync(transcriptPath);
    return Math.ceil(stat.size / 4);
  } catch {
    return 0;
  }
}

function readFile(file) {
  try {
    return fs.readFileSync(file, 'utf8');
  } catch {
    return '';
  }
}

function expandHome(filePath) {
  if (!filePath) return '';

  if (filePath.startsWith('~/')) {
    return path.join(os.homedir(), filePath.slice(2));
  }

  return filePath;
}

function format(value) {
  return Math.round(Number(value || 0)).toLocaleString('en-US');
}

function formatCompact(value) {
  const n = Number(value || 0);

  if (n >= 1000000) return `${(n / 1000000).toFixed(1)}M`;
  if (n >= 1000) return `${(n / 1000).toFixed(1)}K`;

  return String(Math.round(n));
}
